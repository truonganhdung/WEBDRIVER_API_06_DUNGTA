package java_basic;

public class Java_04_Condition_Statement {

	public static void main(String[] args) {
		String name = "aaa";

		switch (name) {
		case "11":
			System.out.println("11");
			break;
		case "22":
			System.out.println("22");
			break;
		default:
			System.out.println("aaa is right");
			break;
		}
	}
}
